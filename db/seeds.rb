# Esvaziar tabelas (opção)
# ------------------------------------------------------------------------------
if ENV["SEED_FORCE_RESET"] == "1"
  $stderr.puts "[seeds] esvaziando ratings, posts, users..."
  [ Rating, Post, User ].each(&:delete_all)
  $stderr.puts "[seeds] esvaziamento concluído"
elsif User.exists? || Post.exists? || Rating.exists?
  $stderr.puts "[seeds] Abortado: as tabelas não estão vazias. Defina SEED_FORCE_RESET=1 para apagar e semear de novo, ou use um banco limpo."
  exit 1
end

