# Execução
# ------------------------------------------------------------------------------
if ENV["SEED_BULK"] == "1"
  $stderr.puts "[seeds] modo=bulk insert_all (NÃO passa pelos controllers) lote #{INSERT_BATCH} POSTS_TARGET=#{POSTS_TARGET}"
  seed_large_via_bulk
else
  $stderr.puts "[seeds] modo=API (requisito enunciado) client=#{api_client_class_name} POSTS_TARGET=#{POSTS_TARGET}"
  seed_large_via_api
end

